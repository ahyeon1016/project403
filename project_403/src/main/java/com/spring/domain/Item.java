package com.spring.domain;

public class Item 
{
	private String change_nick; // 닉네임 변경 아이템
	private String color_change; // 색상 변경 아이템
	
	// getter + setter
	public String getChange_nick() {
		return change_nick;
	}
	public void setChange_nick(String change_nick) {
		this.change_nick = change_nick;
	}
	public String getColor_change() {
		return color_change;
	}
	public void setColor_change(String color_change) {
		this.color_change = color_change;
	}
	
	
}
